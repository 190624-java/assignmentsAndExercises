/*
  3. Return the factorial of n

  f(0) = 1
  f(1) = 1
  f(3) = 6
*/
homework.factorial = function(n){

  let total = 1;

  if(n == 0){
    return 1;
  }
  else{
    for(let i = 1; i <= n; i++){
      total = total * i;
    }
  }
  return total;
};

